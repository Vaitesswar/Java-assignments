// class --> data + functions together to create objects
// public means this function/class is visible and accessible by any other functions/classes.
public class HelloWorld {

    public static void main(String[] args){ // In the command line, the input is defined by a string of arguments ['one, 'two'] args[0] = 'one'.
        // main method is the entry point for all other classes.
        // static means it does not need an object to be called and void means there is no return value for this function.
        // main is function name is String[] is special data type in Java.
        System.out.println("Hello World !"); // ln means next line. System.out means output this text.
        System.out.println("Once more");
    }
}
